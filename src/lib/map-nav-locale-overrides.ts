function deepMergeNav(root: Record<string, unknown>, patch: NavPatch) {
  if (patch.common) {
    const c = (root.common as Record<string, unknown>) ?? {};
    root.common = { ...c, ...patch.common };
  }
  if (!patch.map) return;
  const curMap = (root.map as Record<string, unknown>) ?? {};
  const nextMap = { ...curMap, ...patch.map };
  if (patch.map.maneuver) {
    nextMap.maneuver = {
      ...((curMap.maneuver as Record<string, string>) ?? {}),
      ...patch.map.maneuver,
    } as Record<string, string>;
  }
  root.map = nextMap;
}

/** Merge localized navigation overlay strings (`en` strings live in `i18n.ts`). */
export function applyMapNavLocaleOverrides(resources: Record<string, { translation: Record<string, unknown> }>) {
  for (const [code, patch] of Object.entries(PATCHES)) {
    const pack = resources[code];
    if (!pack?.translation) continue;
    deepMergeNav(pack.translation, patch);
  }
}
