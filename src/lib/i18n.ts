function fillMissing(target: any, source: any): any {
  if (target == null || typeof target !== "object") return target ?? source;
  const out: any = { ...target };
  for (const key of Object.keys(source || {})) {
    const srcVal = source[key];
    const curVal = out[key];
    if (curVal == null) out[key] = srcVal;
    else if (typeof curVal === "object" && typeof srcVal === "object") out[key] = fillMissing(curVal, srcVal);
  }
  return out;
}

